package program;

public class Calculator
{
	public int sum(int _n1, int _n2)
	{
		return _n1 + _n2;
	}
	
	public int subtract(int _n1, int _n2)
	{
		return _n1 - _n2;
	}
	
	public int multiply(int _n1, int _n2)
	{
		return _n1 * _n2;
	}
	
	public float divide(int _n1, int _n2)
	{
		return _n1 / _n2;
	}
}
